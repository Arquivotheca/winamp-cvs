#define DITHER_GEN(x)                                                      \
{                                                                          \
  int32_t tmp0, tmp1;                                                       \
  tmp0 = (((int32_t)(DITHMULT * state->mants_tabls.dithtemp)) << 16) >> 16; \
  state->mants_tabls.dithtemp = (int16_t)tmp0;                              \
  tmp1 = (((int32_t)(DITHMULT * state->mants_tabls.dithtemp)) << 16) >> 16; \
  state->mants_tabls.dithtemp = (int16_t)tmp1;                              \
  x = (0.707106781f * 0.5f * (tmp0 + tmp1));                               \
}

