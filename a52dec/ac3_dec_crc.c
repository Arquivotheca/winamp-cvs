int32_t crcCheck(int32_t num,
                uint8_t *ptr)
{
  uint16_t tmp = 0;
  int32_t i;

  for (i = 0; i < num; i++) {
    tmp = (tmp << 8) ^ ptr[i] ^ crcLut[(tmp >> 8) & 0xff];
  }
  return ((int32_t)tmp);
}

